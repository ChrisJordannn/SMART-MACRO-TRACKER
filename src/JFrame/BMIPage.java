    public class DatabaseHandler {
        private BMICallback callback;
        public DatabaseHandler(BMICallback callback) {
        this.callback = callback;
    }
        // Method to get the last inserted BMI value
        public double getLastInsertedBMI() {
            double lastBMI = 0.0;

            try (Connection con = DBConnection.getConnection();
                 Statement stmt = con.createStatement()) {

                String sql = "SELECT bmi_value FROM bmi ORDER BY bmi_id DESC LIMIT 1";
                ResultSet rs = stmt.executeQuery(sql);

                if (rs.next()) {
                    lastBMI = rs.getDouble("bmi_value");
                }

            } catch (SQLException e) {
                e.printStackTrace();
            }

            return lastBMI;
        }
    }
