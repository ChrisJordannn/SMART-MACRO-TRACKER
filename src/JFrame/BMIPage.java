// method to compute BMI and display result
public void computeAndDisplayBMI() {
    try {
        // Get user input for height and weight
        float height = Float.parseFloat(txt_height.getText());
        float weight = Float.parseFloat(txt_weight.getText());
        // Compute BMI
        float bmi = calculateBMI(height, weight);

        // Display the BMI result in a JTextArea
        txt_BMI.setText("        "+bmi+"        ");
        String remarks = getRemarks(bmi);
        txt_BMI_remarks.setText("Remarks: " + remarks);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values for height and weight.");
    }
}

// method to calculate BMI
        private float calculateBMI(float height, float weight) {
            // BMI formula: weight (kg) / (height (m) * height (m))
            return weight / (height * height);
        }
        private String getRemarks(float bmi) {
            if (bmi < 18.5) {
                return "You are Underweight";
            } else if (bmi >= 18.5 && bmi <= 24.9) {
                return "You are Normal";
            } else if (bmi >= 25 && bmi <= 29.9) {
                return "You are Overweight";
            } else {
                return "You are Obese";
            }
            }
    private void uploadBMIToDatabase(float bmi) {
        try{
            if (SessionManager.isLoggedIn()) {
                User currentUser = SessionManager.getCurrentUser();
                userId = currentUser.getId();
                } else {
                JOptionPane.showMessageDialog(this, "unsuccessful");
            // Handle not logged in
             }
                // Establish database connection (update with your connection details)
                Connection con = DBConnection.getConnection();
                // Insert BMI into the database
                String sql = "INSERT INTO bmi (id, bmi_value) VALUES (?,?)";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setInt(1,userId);
                pst.setFloat(2,bmi);
            // Execute the insert query
                int rowsInserted = pst.executeUpdate();

                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this, "BMI uploaded to the database successfully.");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to upload BMI to the database.");
                }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error uploading BMI to the database:\n" + e.getMessage());
            e.printStackTrace();
        }
    }
    private void saveBMIToDatabase() {
        try {
            // Get the computed BMI from the JTextArea
            String bmiString = txt_BMI.getText().replace("                ", "");
            float bmi = Float.parseFloat(bmiString);

            // Upload BMI to the database
            uploadBMIToDatabase(bmi);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please compute BMI first.");
        }
    }
